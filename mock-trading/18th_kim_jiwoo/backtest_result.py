import pandas as pd
import numpy as np
import matplotlib.pyplot as plt


# ==========================================
# 1. 파일 경로 설정
# ==========================================
price_path = '/content/수정주가.xlsx'
pbr_path = '/content/PBR.xlsx'
roe_path = '/content/ROE.xlsx'

# ==========================================
# 2. 데이터 불러오기 및 전처리
# ==========================================

price = pd.read_excel(price_path, header=9, index_col=0)
pbr = pd.read_excel(pbr_path, header=9, index_col=0)
roe = pd.read_excel(roe_path, header=9, index_col=0)

# 인덱스에서 메타데이터 행 제거 (예: '유형', '아이템코드', '아이템명', '집계주기')
# 실제 데이터는 이 4줄 다음에 시작한다고 가정
price = price.iloc[4:]
pbr = pbr.iloc[4:]
roe = roe.iloc[4:]

# 인덱스를 날짜 형식으로 변환
price.index = pd.to_datetime(price.index)
pbr.index = pd.to_datetime(pbr.index)
roe.index = pd.to_datetime(roe.index)

# 엑셀의 N/A 등 문자열 에러를 숫자로 변환
price = price.apply(pd.to_numeric, errors='coerce').ffill()
pbr = pbr.apply(pd.to_numeric, errors='coerce').ffill()
# ROE 분기데이터
roe = roe.apply(pd.to_numeric, errors='coerce').resample('D').ffill().reindex(price.index).ffill()
pbr = pbr.reindex(price.index).ffill()

# ==========================================
# 3. KOSPI 벤치마크 및 모멘텀 계산
# ==========================================
# 실제 KOSPI 지수 데이터가 없으므로 전체 주식의 평균 흐름을 KOSPI(시장)로 간주
kospi_daily_ret = price.pct_change().mean(axis=1)
kospi_idx = (1 + kospi_daily_ret).cumprod()

# 3개월(약 60영업일) 모멘텀 계산
mom_3m = price.pct_change(60)
kospi_mom_3m = kospi_idx.pct_change(60)

# 매주 금요일 리밸런싱 날짜 추출
rebalance_days = price.resample('W-FRI').last().index
rebalance_days = [d for d in rebalance_days if d in price.index]

# ==========================================
# 4. 백테스트 시뮬레이션 (일간 루프)
# ==========================================

daily_returns = []
current_weights = pd.Series(0.0, index=price.columns)
entry_prices = pd.Series(0.0, index=price.columns)

# 첫 60일은 모멘텀 계산을 위해 스킵
start_idx = 61

for i in range(start_idx, len(price)):
    date = price.index[i]

    # 1) 오늘의 개별 종목 수익률 계산
    today_ret = price.iloc[i] / price.iloc[i-1] - 1
    today_ret = today_ret.fillna(0)

    # 2) [리스크 관리] 진입 후 -10% 이상 하락 시 즉각 손절 로직
    if current_weights.sum() > 0:
        ret_from_entry = price.iloc[i] / entry_prices - 1
        stop_loss_hit = ret_from_entry <= -0.10
        # 손절 조건에 맞으면 비중을 0으로 만들어 매도 처리
        current_weights[stop_loss_hit] = 0.0

    # 3) 오늘 포트폴리오의 최종 수익률 저장
    port_ret = (current_weights * today_ret).sum()
    daily_returns.append(port_ret)

    # 4) [매주 리밸런싱] 금요일마다 종목 교체
    if date in rebalance_days:
        # 조건: 0 < PBR < 1, ROE >= 10, 모멘텀이 코스피보다 강함
        cond_pbr = (pbr.iloc[i] > 0) & (pbr.iloc[i] < 1.0)
        cond_roe = roe.iloc[i] >= 10.0
        cond_mom = mom_3m.iloc[i] > kospi_mom_3m.iloc[i]

        target_stocks = cond_pbr & cond_roe & cond_mom

        if target_stocks.sum() > 0:
            # 타겟 종목들에 동일 가중치(Equal-weight) 배분
            current_weights = target_stocks / target_stocks.sum()
            # 진입 가격 리셋 (새로 샀으므로)
            entry_prices = price.iloc[i].copy()
        else:
            # 살 종목이 없으면 전액 현금 보유
            current_weights = pd.Series(0.0, index=price.columns)

# ==========================================
# 5. 성과 평가 및 시각화
# ==========================================
# 수익률 시리즈화
strat_returns = pd.Series(daily_returns, index=price.index[start_idx:])
market_returns = kospi_daily_ret.loc[strat_returns.index]

# 누적 수익률 계산
cum_strat = (1 + strat_returns).cumprod()
cum_market = (1 + market_returns).cumprod()

# 성과 지표 계산 함수 (CAGR, MDD, Sharpe)
def calc_metrics(ret_series, cum_series):
    cagr = (cum_series.iloc[-1] ** (252 / len(cum_series))) - 1
    vol = ret_series.std() * np.sqrt(252)
    sharpe = cagr / vol if vol != 0 else 0
    roll_max = cum_series.cummax()
    drawdown = cum_series / roll_max - 1
    mdd = drawdown.min()
    return cagr, vol, sharpe, mdd

strat_cagr, strat_vol, strat_sharpe, strat_mdd = calc_metrics(strat_returns, cum_strat)
mkt_cagr, mkt_vol, mkt_sharpe, mkt_mdd = calc_metrics(market_returns, cum_market)

# 그래프 그리기
plt.figure(figsize=(12, 6))
plt.plot(cum_strat, label='Quality & Momentum Strategy', color='royalblue', linewidth=2)
plt.plot(cum_market, label='KOSPI Benchmark', color='darkorange', alpha=0.8)
plt.title('Portfolio vs KOSPI Cumulative Return (Weekly Rebalancing & Stop-loss)')
plt.ylabel('Cumulative Return')
plt.grid(True, linestyle='--', alpha=0.6)
plt.legend()
plt.show()

